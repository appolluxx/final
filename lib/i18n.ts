export const useTranslations = (language: 'en' | 'th') => {
  return useMemo(() => {
    const t = (key: string) => {
      return translations[language][key] || key;
    };
    const T = (key: string) => {
        // Specifically for sectors and regions which have their own nested objects
        if (key in regionTranslations[language]) {
            return regionTranslations[language][key as Region];
        }
        return translations[language][key] || key;
    }
    return { t, T };
  }, [language]);
};